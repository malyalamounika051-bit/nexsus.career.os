// SEED FUNCTION
// ═══════════════════════════════════════════════════════════════

/**
 * Seed curated opportunities into the database.
 * Uses upsert by applicationUrl to avoid duplicates on re-seed.
 * @returns {{ inserted: number, updated: number, total: number }}
 */
const seedCuratedOpportunities = async () => {
  console.log(`📦 Seeding ${CURATED_OPPORTUNITIES.length} curated opportunities...`);
  
  let inserted = 0;
  let updated = 0;

  for (const opp of CURATED_OPPORTUNITIES) {
    try {
      const result = await Opportunity.findOneAndUpdate(
        { applicationUrl: opp.applicationUrl },
        {
          $set: {
            title: opp.title,
            organization: opp.organization,
            type: opp.type,
            description: opp.description,
            deadline: opp.deadline || futureDate(60),
            applicationUrl: opp.applicationUrl,
            eligibility: opp.eligibility || '',
            requiredSkills: opp.requiredSkills || [],
            preferredSkills: opp.preferredSkills || [],
            location: opp.location || 'Remote',
            remote: opp.remote !== undefined ? opp.remote : true,
            tags: opp.tags || [],
            source: opp.source || 'Curated',
            sourceType: 'curated',
            sourceScore: opp.sourceScore || 80,
            verificationStatus: 'verified',
            isVerified: true,
            lastVerified: new Date(),
            opportunityStatus: 'active',
            benefits: opp.benefits || [],
            difficulty: opp.difficulty || 'Medium',
            estimatedCommitment: opp.estimatedCommitment || '',
            isFeatured: opp.isFeatured || false,
            prizePool: opp.prizePool || ''
          },
          $setOnInsert: {
            applicationCount: 0,
            participantCount: 0
          }
        },
        { upsert: true, new: true }
      );

      if (result.createdAt && (new Date() - result.createdAt) < 5000) {
        inserted++;
      } else {
        updated++;
      }
    } catch (err) {
      console.error(`Error seeding "${opp.title}":`, err.message);
    }
  }

  console.log(`📦 Seed complete: ${inserted} new, ${updated} updated, ${CURATED_OPPORTUNITIES.length} total.`);
  return { inserted, updated, total: CURATED_OPPORTUNITIES.length };
};

module.exports = { seedCuratedOpportunities };
